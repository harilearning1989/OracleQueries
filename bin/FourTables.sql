CREATE TABLE EMPLOYEE_INFO
   (EMP_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 
	INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	EMP_NAME VARCHAR2(100 BYTE), 
	FATHER_NAME VARCHAR2(100 BYTE), 
	GENDER VARCHAR2(10 BYTE), 
	CATEGORY VARCHAR2(10 BYTE), 
	MOBILE NUMBER, 
	EMP_AGE NUMBER, 
	DOB DATE, 
	SALARY NUMBER, 
	CURRENT_LOCATION VARCHAR2(100 BYTE), 
	EMP_STATUS CHAR(1 BYTE), 
	OFFICE_NAME VARCHAR2(100 BYTE), 
	OFFICE_STATUS VARCHAR2(100 BYTE), 
	PINCODE NUMBER, 
	TELEPHONE_NO VARCHAR2(20 BYTE), 
	TALUK VARCHAR2(100 BYTE), 
	DISTRICT VARCHAR2(100 BYTE), 
	STATE_AP VARCHAR2(100 BYTE), 
	POSTAL_DIVISION VARCHAR2(100 BYTE), 
	POSTAL_REGION VARCHAR2(100 BYTE), 
	POSTAL_CIRCLE VARCHAR2(100 BYTE)
   ) 
  
 ===================================================================
 
 CREATE TABLE EMPLOYEE_MANAGER 
   (	EMP_ROW_ID NUMBER(*,0), 
	EMP_ID NUMBER(*,0), 
	FIRST_NAME VARCHAR2(50 BYTE), 
	LAST_NAME VARCHAR2(50 BYTE), 
	MIDDLE_NAME VARCHAR2(50 BYTE), 
	DATE_TIME_BIRTH DATE, 
	DATE_OF_JOIN VARCHAR2(15 BYTE), 
	DATE_OF_RELEASE VARCHAR2(15 BYTE), 
	DAYS_WORKED LONG, 
	GENDER CHAR(1 BYTE), 
	AGE NUMBER(*,0), 
	SALARY NUMBER(*,0), 
	MANAGER_ID NUMBER(*,0), 
	DEPARTMENT VARCHAR2(50 BYTE), 
	EMAIL VARCHAR2(50 BYTE), 
	EMP_STATUS CHAR(1 BYTE), 
	COUNTRY VARCHAR2(100 BYTE), 
	 CONSTRAINT EMP_ID_PK PRIMARY KEY (EMP_ID),
	 CONSTRAINT COUNTRY_FK FOREIGN KEY (COUNTRY)
	  REFERENCES COUNTRY_REGIONS (COUNTRY_NAME)
   ) 
   
 =====================================================================
 
 

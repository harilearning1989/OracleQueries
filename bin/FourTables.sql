CREATE TABLE COUNTRY_REGIONS
   (	
   COUNTRY_ID NUMBER(*,0), 
	COUNTRY_NAME VARCHAR2(500 BYTE), 
	ALPHA2 VARCHAR2(10 BYTE), 
	ALPHA3 VARCHAR2(10 BYTE), 
	COUNTRY_CODE NUMBER, 
	REGION VARCHAR2(30 BYTE), 
	SUB_REGION VARCHAR2(40 BYTE), 
	INTERMEDIATE_REGION VARCHAR2(30 BYTE), 
	REGION_CODE NUMBER, 
	SUB_REGION_CODE NUMBER, 
	INTERMEDIATE_REGION_CODE NUMBER, 
	 CONSTRAINT COUNTRY_NAME_PK PRIMARY KEY (COUNTRY_NAME)
  )

